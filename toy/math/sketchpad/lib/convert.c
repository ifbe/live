char convert(char old)
{
    char* p;
    u64 rsi;
    char new=0x20;
    int i;

    rsi=(u64)&translatetable;
    for(i=0;i<44;i++)
    {
        p=(char*)rsi;
        if(*p==old)
        {
            rsi++;
            p=(char*)rsi;
            new=*p;
            break;
        }
        rsi+=2;
    }
    return new;
}
